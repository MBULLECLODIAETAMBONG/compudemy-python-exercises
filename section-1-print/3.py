# Assign to the variable age number 20. 
# Using the age variable and the print() function print to the console the following text:
# I am 20 years old.

age = 20
print("I am ", age, "years old")

# using f-strings / strings interpolation
print("using f-strings / strings interpolation")
print(f"I am {age} years old")

# type casting 
print("type casting ")
print("I am " + str(age) + " years old")
